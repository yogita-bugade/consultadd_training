'''If one data type value is assigned to ‘a’ variable and then a different data type value is assigned to ‘a’
again. Will it change the value? If Yes then Why?
-- Yes, assigning a different data type value to the variable 'a' will change its value.
In most programming languages, when you assign a new value to a variable,
it replaces the previous value stored in that variable.
The variable 'a' essentially acts as a placeholder in memory, and its content can be
overwritten with a new value of any data type.'''
a = 5
print(a)

a = "Hello"
print(a)
